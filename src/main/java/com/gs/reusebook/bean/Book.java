package com.gs.reusebook.bean;

/**
 * 
 * 书籍为一种商品，书籍信息
 * 
 * @author IceAsh
 *
 */
public class Book {
	private String name;
	private String isbn;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	
	
}
